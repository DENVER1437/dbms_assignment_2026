const HISTORY_KEY='normPro_history';

function saveToHistory(schema,fdRaw){
  let history=JSON.parse(localStorage.getItem(HISTORY_KEY)||'[]');
  const entry={id:Date.now(),schema,fds:fdRaw,time:new Date().toLocaleString()};
  history=history.filter(h=>h.schema!==schema);
  history.unshift(entry);
  history=history.slice(0,5);
  localStorage.setItem(HISTORY_KEY,JSON.stringify(history));
  renderHistory();

  const list=document.getElementById('history-list');
  const arrow=document.getElementById('history-arrow');
  if(list&&list.style.display==='none'){
    list.style.display='flex';
    list.style.flexDirection='column';
    if(arrow)arrow.textContent='▾';
  }
}

function renderHistory(){
  const list = document.getElementById('history-list');
  if (!list) return;
  const history = JSON.parse(localStorage.getItem(HISTORY_KEY) || '[]');

  if (!history.length) {
    list.innerHTML = `<div style="display:flex;align-items:center;gap:.6rem;color:var(--text3);font-size:.82rem;font-style:italic;"><span style="font-size:1rem;">🕐</span>No history yet — run a normalization to save it here.</div>`;
    return;
  }

  list.innerHTML = `
    <div style="font-size:.72rem;color:var(--text3);text-transform:uppercase;letter-spacing:.07em;margin-bottom:.5rem;font-weight:600;">Last ${history.length} schema${history.length > 1 ? 's' : ''}</div>
    <div style="display:flex;flex-wrap:wrap;gap:.5rem;">
      ${history.map(h => {
        const name = h.schema.match(/^(\w+)\s*\(/)?.[1] || h.schema.substring(0, 18);
        const attrCount = (h.schema.match(/,/g) || []).length + 1;
        return `
          <div class="history-chip" title="Loaded at ${h.time} — Click to restore">
            <span onclick="loadFromHistory('${h.id}')" style="display:flex;align-items:center;gap:.4rem;">
              <span style="background:rgba(124,58,237,.15);color:var(--purple);border-radius:4px;font-size:.68rem;font-weight:700;padding:.1rem .4rem;">${attrCount} attrs</span>
              ${name}
            </span>
            <span class="del-btn" onclick="deleteHistory('${h.id}')" title="Remove from history">✕</span>
          </div>`;
      }).join('')}
    </div>`;
}

function loadFromHistory(id){
  const history=JSON.parse(localStorage.getItem(HISTORY_KEY)||'[]');
  const entry=history.find(h=>String(h.id)===String(id));
  if(!entry)return;
  document.getElementById('schema-input').value=entry.schema;
  document.getElementById('fd-input').value=entry.fds;
  updateFDIndicators();
}

function deleteHistory(id){
  let history=JSON.parse(localStorage.getItem(HISTORY_KEY)||'[]');
  history=history.filter(h=>String(h.id)!==String(id));
  localStorage.setItem(HISTORY_KEY,JSON.stringify(history));
  renderHistory();
}

