function updateFDIndicators() {
  const textarea = document.getElementById('fd-input');
  const container = document.getElementById('fd-indicators');
  if (!container || !textarea) return;

  const schemaRaw = document.getElementById('schema-input').value.trim();
  const schema = schemaRaw ? parseSchema(schemaRaw) : null;
  const knownAttrs = schema ? new Set(schema.attrs) : null;

  const lines = textarea.value.split('\n');

  // Measure actual line height of textarea
  // Use computed style for accuracy
  const computed = window.getComputedStyle(textarea);
  const lineHeightPx = parseFloat(computed.lineHeight) || 22;
  const paddingTop = parseFloat(computed.paddingTop) || 12;

  container.innerHTML = '';
  container.style.paddingTop = paddingTop + 'px';

  lines.forEach((line) => {
    const dot = document.createElement('div');
    dot.style.cssText = `width: 7px; height: 7px; border-radius: 50%; margin-bottom: ${lineHeightPx - 7}px; margin-top: 0px; flex-shrink: 0;`;

    const trimmed = line.trim();

    if (!trimmed) {
      dot.style.background = 'transparent';
      dot.title = '';
    } else {
      const hasSep = trimmed.includes('->') || trimmed.includes('→');
      if (!hasSep) {
        dot.style.background = 'var(--red)';
        dot.style.boxShadow = '0 0 4px rgba(239,68,68,.5)';
        dot.title = 'Missing "->" separator';
      } else {
        const parsed = parseFDs(trimmed);
        const fd = parsed[0];
        if (!fd || !fd.lhs.length || !fd.rhs.length) {
          dot.style.background = 'var(--red)';
          dot.style.boxShadow = '0 0 4px rgba(239,68,68,.5)';
          dot.title = 'Invalid FD format';
        } else if (knownAttrs) {
          const unknowns = [...fd.lhs, ...fd.rhs].filter(a => !knownAttrs.has(a));
          if (unknowns.length) {
            dot.style.background = 'var(--gold)';
            dot.style.boxShadow = '0 0 4px rgba(245,158,11,.5)';
            dot.title = `Unknown attributes: ${unknowns.join(', ')}`;
          } else {
            dot.style.background = 'var(--green)';
            dot.style.boxShadow = '0 0 4px rgba(16,185,129,.4)';
            dot.title = 'Valid FD';
          }
        } else {
          dot.style.background = 'var(--teal)';
          dot.style.boxShadow = '0 0 4px rgba(0,229,196,.4)';
          dot.title = 'Valid syntax';
        }
      }
    }
    container.appendChild(dot);
  });
}

function handleFileUpload(event) {
  const file = event.target.files[0];
  if (!file) return;

  const allowedTypes = [
    'text/plain',
    'application/pdf',
    'application/vnd.openxmlformats-officedocument.wordprocessingml.document',
    'application/msword',
  ];
  const allowedExts = ['.txt', '.pdf', '.docx', '.doc', '.text'];
  const ext = '.' + file.name.split('.').pop().toLowerCase();

  if (!allowedTypes.includes(file.type) && !allowedExts.includes(ext)) {
    showUploadStatus('error', `❌ Unsupported file type "${ext}". Please upload a .txt, .pdf, .docx, or .doc file.`);
    event.target.value = '';
    return;
  }

  showUploadStatus('loading', `⏳ Reading "${file.name}"...`);

  if (ext === '.txt' || file.type === 'text/plain') {
    const reader = new FileReader();
    reader.onload = (e) => processUploadedText(e.target.result, file.name);
    reader.onerror = () => showUploadStatus('error', '❌ Could not read file. Please try again.');
    reader.readAsText(file);
  } else if (ext === '.pdf') {
    const reader = new FileReader();
    reader.onload = (e) => {
      try {
        const bytes = new Uint8Array(e.target.result);
        let text = '';
        for (let i = 0; i < bytes.length; i++) {
          const b = bytes[i];
          if (b >= 32 && b <= 126) text += String.fromCharCode(b);
          else if (b === 10 || b === 13) text += '\n';
        }
        text = text.replace(/[^\x20-\x7E\n]/g, ' ').replace(/\s{3,}/g, '\n').trim();
        if (text.length < 20) {
          showUploadStatus('error', '❌ This PDF appears to be scanned/image-based and cannot be read as text. Please copy-paste the content manually.');
          return;
        }
        processUploadedText(text, file.name);
      } catch(err) {
        showUploadStatus('error', '❌ Could not read PDF. Please copy-paste the content manually.');
      }
    };
    reader.readAsArrayBuffer(file);
  } else if (ext === '.docx' || ext === '.doc') {
    const reader = new FileReader();
    reader.onload = (e) => {
      try {
        const bytes = new Uint8Array(e.target.result);
        let raw = '';
        for (let i = 0; i < bytes.length; i++) {
          raw += String.fromCharCode(bytes[i]);
        }
        const matches = raw.match(/<w:t[^>]*>([^<]+)<\/w:t>/g) || [];
        let text = matches.map(m => m.replace(/<[^>]+>/g, '')).join(' ');

        if (text.length < 20) {
          text = '';
          for (let i = 0; i < bytes.length; i++) {
            const b = bytes[i];
            if (b >= 32 && b <= 126) text += String.fromCharCode(b);
            else if (b === 10 || b === 13) text += '\n';
          }
          text = text.replace(/\s{3,}/g, '\n').trim();
        }

        if (text.length < 10) {
          showUploadStatus('error', '❌ Could not extract text from this Word document. Please copy-paste the content manually.');
          return;
        }
        processUploadedText(text, file.name);
      } catch(err) {
        showUploadStatus('error', '❌ Could not read Word document. Please copy-paste the content manually.');
      }
    };
    reader.readAsArrayBuffer(file);
  }

  event.target.value = '';
}

function processUploadedText(text, filename) {
  const schemaPatterns = [
    /([A-Za-z]\w*)\s*\(\s*([\w\s,]+)\s*\)/,           // R(A, B, C)
    /[Ss]chema\s*[:\-]?\s*([A-Za-z]\w*)\s*\(\s*([\w\s,]+)\s*\)/, // Schema: R(A,B,C)
    /[Rr]elation\s*[:\-]?\s*([A-Za-z]\w*)\s*\(\s*([\w\s,]+)\s*\)/, // Relation: R(...)
  ];

  let foundSchema = null;
  for (const pattern of schemaPatterns) {
    const m = text.match(pattern);
    if (m) {
      const attrs = m[2].split(',').map(a => a.trim()).filter(Boolean);
      if (attrs.length >= 2) {
        foundSchema = `${m[1]}(${attrs.join(', ')})`;
        break;
      }
    }
  }

  const fdPattern = /([A-Za-z]\w*(?:\s*,\s*[A-Za-z]\w*)*)\s*(?:->|→|=>|—>)\s*([A-Za-z]\w*(?:\s*,\s*[A-Za-z]\w*)*)/g;
  const fdLines = [];
  let match;
  while ((match = fdPattern.exec(text)) !== null) {
    const lhs = match[1].trim();
    const rhs = match[2].trim();
    if (lhs && rhs) {
      fdLines.push(`${lhs} -> ${rhs}`);
    }
  }

  if (!foundSchema && fdLines.length === 0) {
    showUploadStatus('error',
      `❌ Invalid input — no schema or functional dependencies found in "${filename}".<br>
       <span style="font-size:.78rem;">The file must contain a relation schema like <code>R(A, B, C)</code> and FDs like <code>A -> B</code>.</span>`
    );
    return;
  }

  if (!foundSchema && fdLines.length > 0) {
    showUploadStatus('warn',
      `⚠ FDs extracted (${fdLines.length} found) but no schema detected in "${filename}". Please enter the schema manually.`
    );
    document.getElementById('fd-input').value = fdLines.join('\n');
    updateFDIndicators();
    return;
  }

  if (foundSchema && fdLines.length === 0) {
    showUploadStatus('warn',
      `⚠ Schema extracted from "${filename}" but no FDs detected. Please enter FDs manually.`
    );
    document.getElementById('schema-input').value = foundSchema;
    updateFDIndicators();
    return;
  }

  document.getElementById('schema-input').value = foundSchema;
  document.getElementById('fd-input').value = fdLines.join('\n');
  updateFDIndicators();

  showUploadStatus('success',
    `✓ Extracted from "${filename}" — Schema: <strong>${foundSchema.split('(')[0]}</strong> with ${fdLines.length} FD${fdLines.length > 1 ? 's' : ''}. Review the fields below before running.`
  );
}

