function generateSQL(){
  if(!_lastBCNF||!_lastBCNF.length){alert('Run normalization first.');return;}
  let sql='-- Generated by NormalizerPro\n-- BCNF Decomposition SQL\n\n';
  _lastBCNF.forEach((rel,i)=>{
    const keys=findCandidateKeys(rel.attrs,rel.fds);
    const pk=keys.length?keys[0]:[rel.attrs[0]];
    sql+=`CREATE TABLE ${rel.name} (\n`;
    rel.attrs.forEach(attr=>{
      const lower=attr.toLowerCase();
      let type='VARCHAR(100)';
      if(lower.includes('id'))type='INT';
      if(lower.includes('date')||lower.includes('time'))type='DATE';
      if(lower.includes('salary')||lower.includes('price')||lower.includes('fare'))type='DECIMAL(10,2)';
      if(lower.includes('qty')||lower.includes('quantity')||lower.includes('count'))type='INT';
      sql+=`  ${attr.padEnd(20)} ${type}`;
      if(pk.includes(attr))sql+='  NOT NULL';
      sql+=',\n';
    });
    sql+=`  PRIMARY KEY (${pk.join(', ')})`;
    _lastBCNF.forEach((other,j)=>{
      if(j===i)return;
      const oKeys=findCandidateKeys(other.attrs,other.fds);
      const oPK=oKeys.length?oKeys[0]:[other.attrs[0]];
      const shared=oPK.filter(a=>rel.attrs.includes(a)&&!pk.includes(a));
      if(shared.length===oPK.length&&shared.length>0)sql+=`,\n  FOREIGN KEY (${shared.join(', ')}) REFERENCES ${other.name}(${shared.join(', ')})`;
    });
    sql+='\n);\n\n';
  });
  document.getElementById('sql-output').textContent=sql;
  document.getElementById('sql-modal').style.display='flex';
}

