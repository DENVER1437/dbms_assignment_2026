function showPage(page) {
  // Get all page containers
  const pageHome  = document.getElementById('page-home');
  const pageTool  = document.getElementById('page-tool');
  const pageLearn = document.getElementById('page-learn');

  // Also handle non-wrapped tool content (hero + main + footer)
  const hero   = document.querySelector('.hero');
  const mainEl = document.querySelector('main');
  const footer = document.querySelector('footer');

  // Hide ALL pages first
  if (pageHome)  pageHome.style.display  = 'none';
  if (pageTool)  pageTool.style.display  = 'none';
  if (pageLearn) pageLearn.style.display = 'none';
  if (hero   && !pageTool) hero.style.display   = 'none';
  if (mainEl && !pageTool) mainEl.style.display  = 'none';
  if (footer && !pageTool) footer.style.display  = 'none';

  // Show the correct page
  if (page === 'home') {
    if (pageHome) pageHome.style.display = 'flex';
  } else if (page === 'learn') {
    if (pageLearn) pageLearn.style.display = 'block';
    else if (pageTool) pageTool.style.display = 'block';
  } else {
    // Tool page
    if (pageTool) {
      pageTool.style.display = 'block';
    } else {
      if (hero)   hero.style.display   = 'block';
      if (mainEl) mainEl.style.display = 'block';
      if (footer) footer.style.display = 'block';
    }
  }

  // ── UPDATE NAV TAB ACTIVE STATES ──────────────────────────
  // Remove active class from ALL nav buttons first
  const allNavBtns = document.querySelectorAll('.nav-tab');
  allNavBtns.forEach(btn => {
    btn.classList.remove('nav-tab-active');
    // Also reset inline styles that might override class
    btn.style.background    = '';
    btn.style.borderColor   = '';
    btn.style.color         = '';
  });

  // Add active class ONLY to the correct button
  const activeBtn = document.getElementById('nav-' + page);
  if (activeBtn) {
    activeBtn.classList.add('nav-tab-active');
  }
  // ──────────────────────────────────────────────────────────

  window.scrollTo({ top: 0, behavior: 'smooth' });
}

